package com.szyciov.passenger.entity;

public class Passenger {

	private String headportraitmin;
	
	private String account;
	
	private String nickname;
	
	private String sex;
	
	private String company;
	
	private String dpet;

	public String getHeadportraitmin() {
		return headportraitmin;
	}

	public void setHeadportraitmin(String headportraitmin) {
		this.headportraitmin = headportraitmin;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

	public String getNickname() {
		return nickname;
	}

	public void setNickname(String nickname) {
		this.nickname = nickname;
	}

	public String getSex() {
		return sex;
	}

	public void setSex(String sex) {
		this.sex = sex;
	}

	public String getCompany() {
		return company;
	}

	public void setCompany(String company) {
		this.company = company;
	}

	public String getDpet() {
		return dpet;
	}

	public void setDpet(String dpet) {
		this.dpet = dpet;
	}
}
